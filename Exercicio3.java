public class Exercicio3 {
    public static void main(String[] args) {
        System.out.println("--- Contagem de Clientes (Simulação de 1 a 10) ---");
        
        // Usando um laço 'for' para simular a contagem de clientes
        for (int cliente = 1; cliente <= 10; cliente++) {
            System.out.println("Cliente número: " + cliente + " entrou na loja.");
        }
        
        System.out.println("Contagem da hora finalizada.");
    }
}
